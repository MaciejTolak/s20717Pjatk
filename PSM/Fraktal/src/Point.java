public class Point {
    private double x;
    private double y;
    private double alfa;

    public Point(double x, double y, double alfa) {
        this.x = x;
        this.y = y;
        this.alfa = alfa;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getAlfa() {
        return alfa;
    }
}
